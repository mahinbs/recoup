    <ul class="list-disc pl-6 space-y-2 mb-10">
      <li>PFO is a relatively new, minimally invasive surgical procedure that can help relieve knee pain and improve function in patients with mild to moderate severity of medial compartment OA.</li>
      <li>PFO shifts the loading force from the medial side to the lateral side.</li>
      <li>PFO can also help improve the varus deformity (bowlegs) and slow the progression of OA.</li>
      <li>A small piece of the fibula is removed through a small incision, limited dissection, and no internal fixation.</li>
      <li>Studies have shown that PFO can significantly improve pain, function, and other metrics.</li>
      <li>The femorotibial angle improves by around 7°.</li>
      <li>The hip-knee ankle angle improves by around 6°.</li>
    </ul>

